"""Results store: append eval numbers and regenerate the results table (M4).

The harness writes each evaluation as one JSON record; the markdown report is
*derived* from that store so it can be regenerated deterministically and always
reflects every run we've done. A record is keyed by
``(run, test_set, seed, beam)`` so re-running an eval overwrites rather than
duplicates.

**Seed-variance protocol** (CLAUDE.md rule #6, ADR-008): a single seed's score
is a point estimate with real run-to-run noise. When two or more seeds exist for
the same (run, test_set), the table reports **mean ± population std** so we only
ever compare *distributions*, not lucky single runs. With one seed it shows the
bare number and a seed count of 1 — a visible reminder the variance is unknown.
"""

from __future__ import annotations

import json
import statistics
from collections import defaultdict
from pathlib import Path

METRICS = ("chrf", "bleu", "comet")


def _key(r: dict) -> tuple:
    return (r["run"], r["test_set"], r["seed"], r.get("beam", 1))


def load(path: Path) -> list[dict]:
    return json.loads(Path(path).read_text(encoding="utf-8")) if Path(path).exists() else []


def upsert(records: list[dict], new: dict) -> list[dict]:
    """Add ``new``, replacing any existing record with the same key."""
    k = _key(new)
    for i, r in enumerate(records):
        if _key(r) == k:
            records[i] = new
            return records
    records.append(new)
    return records


def save(path: Path, records: list[dict]) -> None:
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    Path(path).write_text(
        json.dumps(records, indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
    )


def _agg(recs: list[dict], metric: str) -> str:
    vals = [r["metrics"][metric] for r in recs if r["metrics"].get(metric) is not None]
    if not vals:
        return "—"
    if len(vals) == 1:
        return f"{vals[0]:.2f}"
    # ≥2 seeds: report mean ± population std (we compare distributions, not runs).
    return f"{statistics.mean(vals):.2f} ± {statistics.pstdev(vals):.2f}"


def render_markdown(records: list[dict]) -> str:
    lines = [
        "# M4 — Evaluation results",
        "",
        "Auto-generated by `scripts/evaluate.py` from `docs/reports/m4-results.json`.",
        "chrF = iteration metric, BLEU = comparability, COMET = headline (ADR-008).",
        "Scores with ≥2 seeds are **mean ± std**; a single seed shows the bare number.",
        "",
        "| run | test set | beam | seeds | chrF | BLEU | COMET |",
        "|:--|:--|--:|--:|--:|--:|--:|",
    ]
    groups: dict[tuple, list[dict]] = defaultdict(list)
    for r in records:
        groups[(r["run"], r["test_set"], r.get("beam", 1))].append(r)

    for (run, test_set, beam), recs in sorted(groups.items()):
        n_seeds = len({r["seed"] for r in recs})
        cells = " | ".join(_agg(recs, m) for m in METRICS)
        lines.append(f"| {run} | {test_set} | {beam} | {n_seeds} | {cells} |")

    lines += [
        "",
        "Metric directions: chrF/BLEU are 0–100 (higher better); COMET is ~0–1 "
        "(higher better). BLEU signature is recorded per-record in the JSON.",
        "",
    ]
    return "\n".join(lines)
